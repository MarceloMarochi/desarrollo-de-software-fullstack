// FUNCIONES

// 3 Formas de definir funciones
// Normal
function sumar(a, b) {
    return a + b
}

// Funcion flecha
sumar2 = (a, b) => {
    return a + b
}

// Una mezcla de las dos
sumar3 = function(a, b) {
    return a + b
}

console.log(sumar(2,1))
console.log(sumar2(2,1))
console.log(sumar3(2,1))


